const age = 18;
const userAge = "18";

// Loose Equality
console.log("Loose Equality (==): ", userAge == age);

// Strict Equality
console.log("Strict Equality (===): ", userAge === age);

// Loose Inequality
console.log("Loose Inequality (!==): ", userAge !== age);

// Relational Operators
console.log("Is 1 greater than 2? ", 1 > 2);
console.log("Is 5 less  than or equal to 5? ", 5 <= 5);
